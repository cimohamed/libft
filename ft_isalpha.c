#include "libft.h"

int	ft_isalpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!((is_lower(str[i])) || (is_upper(str[i]))))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
